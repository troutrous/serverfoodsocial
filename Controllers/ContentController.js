const ContentModel = require("../Models/ContentModel");

module.exports.GetContentByID = (req, res) => {
    ContentModel.GetContentByID(req.body, (err, data) => {
        if (err) {
            res.status(401).json({
                message: err.message || "Something has been error...",
            });
            return;
        } else {
            res.status(200).json(
                data
            );
            return;
        }
    });
};

